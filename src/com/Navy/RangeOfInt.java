package com.Navy;

public class RangeOfInt {
    public static void main(String[] args) {
        System.out.println(Integer.MAX_VALUE);
        float f1=4.5F;
        float f2=5;
        System.out.println(f1);
        System.out.println(f2);
        double d1=10.56D;
        System.out.println(d1);

    }
}
